const YMD_RE = /^(\d{4})-(\d{2})-(\d{2})$/;

function parseYearOrThrow(ymd: string): number {
  const m = YMD_RE.exec(ymd);
  if (!m) throw new Error(`invalid YYYY-MM-DD: ${ymd}`);
  return Number(m[1]);
}

function assertSupportedYear(year: number, ymd: string): void {
  if (year < SUPPORTED_YEAR_RANGE.min || year > SUPPORTED_YEAR_RANGE.max) {
    throw new Error(
      `祝日マスター範囲外 (${SUPPORTED_YEAR_RANGE.min}–${SUPPORTED_YEAR_RANGE.max}): ${ymd}`,
    );
  }
}

/** `YYYY-MM-DD` が祝日 / 振替休日 / 国民の休日のいずれかなら true。 */
export function isHoliday(ymd: string): boolean {
  const year = parseYearOrThrow(ymd);
  assertSupportedYear(year, ymd);
  return HOLIDAYS.has(ymd);
}

/** 祝日名 (該当しない場合は null)。UI ツールチップ用。 */
export function holidayName(ymd: string): string | null {
  const year = parseYearOrThrow(ymd);
  assertSupportedYear(year, ymd);
  return HOLIDAYS.get(ymd) ?? null;
}

/**
 * `YYYY-MM-DD` から `DayKind` (weekday / saturday / sunday_holiday) を返す。
 *
 * 判定優先順位:
 *   1. 祝日・振替休日・国民の休日 → SUNDAY_HOLIDAY
 *   2. 曜日が日曜 → SUNDAY_HOLIDAY
 *   3. 曜日が土曜 → SATURDAY
 *   4. それ以外 → WEEKDAY
 *
 * 「土曜が祝日」のケースは SUNDAY_HOLIDAY 扱い (シフト枠の必要人員数を日祝側に揃える方が
 * 現場運用と合致する) とする。
 */
export function dayKindFor(ymd: string): DayKind {
  if (isHoliday(ymd)) return DayKind.SUNDAY_HOLIDAY;
  // JST 基準で曜日を取得。fromJstYmd 経由で UTC 0:00 = JST 9:00 の Date を作り、
  // toLocaleDateString({ timeZone: "Asia/Tokyo", weekday: "short" }) で確実に JST の曜日を読む。
  const d = new Date(`${ymd}T00:00:00.000Z`);
  // UTC の曜日 (Date#getUTCDay) は JST 00:00 と同じ日を指すので、そのまま使う。
  const dow = d.getUTCDay();
  if (dow === 0) return DayKind.SUNDAY_HOLIDAY;
  if (dow === 6) return DayKind.SATURDAY;
  return DayKind.WEEKDAY;
}

/**
 * 指定月 ("YYYY-MM") に含まれる祝日の日付一覧 ("YYYY-MM-DD") を返す。
 * 自動作成 run の stats.input.holidays に渡す用途。
 */
export function holidaysInMonth(ym: string): string[] {
  if (!/^\d{4}-(0[1-9]|1[0-2])$/.test(ym)) {
    throw new Error(`invalid YYYY-MM: ${ym}`);
  }
  const year = Number(ym.slice(0, 4));
  assertSupportedYear(year, `${ym}-01`);
  const prefix = `${ym}-`;
  const out: string[] = [];
  for (const ymd of HOLIDAYS.keys()) {
    if (ymd.startsWith(prefix)) out.push(ymd);
  }
  out.sort();
  return out;
}
